    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:

//        Limpia el combobox nombreslista
        nombreslista.removeAllItems();

//        Recorre el HashSet Planeta
        for (Planeta planeta : allplanet) {
//            Dentro del combobox nombreslista se le a√±ade los nombres de planeta
            nombreslista.addItem(planeta.getName());

//            Definimos una variable String como el item seleccionado de combobox
            String nombre = (String) nombreslista.getSelectedItem();

//            Consigo el objeto Planeta mediante el nombre
            Planeta p = getHashCodePlanet(new Planeta(nombre));

//            Imprimo la informacion del objeto planeta en info
            info.setText(p.toString());
        }


    }//GEN-LAST:event_formWindowOpened

    private void filtroPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_filtroPopupMenuWillBecomeInvisible
        // TODO add your handling code here:
        nombreslista.removeAllItems();
        int cont = 0;

        if (filtro.getSelectedItem().toString().equalsIgnoreCase("Planetas")) {
            for (Planeta p : allplanet) {
                nombreslista.addItem(p.getName());
                cont++;
                String nombre = (String) nombreslista.getSelectedItem();
                info.setText(p.toString());
                System.out.println("PLANETAS");
            }
        } else if (filtro.getSelectedItem().toString().equalsIgnoreCase("Ciudadanos")) {
            for (Planeta planeta : allplanet) {
                for (Ser ser : planeta.getPopulation()) {
                    nombreslista.addItem(ser.getName());
                    if (ser.getName().equals(nombreslista.getSelectedItem())) {
                        Planeta p = getPlanetaSer(ser);
                        info.setText(ser.toString() + "\n" + "Planeta: " + p.getName());
                        System.out.println(ser);
                    }
                    cont++;
                    System.out.println("CIUDADANOS");
                }
            }
            if (cont == 0) {
                System.out.println("NO EXISTEN CIUDADANOS");
                for (Planeta p : allplanet) {
                    nombreslista.addItem(p.getName());
                    cont++;
                    String nombre = (String) nombreslista.getSelectedItem();
                    info.setText(p.toString());
                    System.out.println("PLANETAS");
                }

                JOptionPane.showMessageDialog(this, "No hay ciudadanos", "Ciudadanos Insuficientes", JOptionPane.WARNING_MESSAGE);
                filtro.setSelectedIndex(0);

            }
        }
    }//GEN-LAST:event_filtroPopupMenuWillBecomeInvisible

    private void nombreslistaPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_nombreslistaPopupMenuWillBecomeInvisible
        // TODO add your handling code here:

        String nombre = (String) nombreslista.getSelectedItem();
        if (filtro.getSelectedItem().equals("Planetas")) {
            Planeta p = getHashCodePlanet(new Planeta(nombre));
            info.setText(p.toString());
        }

        if (filtro.getSelectedItem().toString().equalsIgnoreCase("Ciudadanos")) {
            Ser s = getHashCodeSer(new Ser(nombre));
            System.out.println(s);
            Planeta p = getPlanetaSer(s);
            info.setText(s.toString() + "\n" + "Planeta: " + p.getName()
            );
        }
    }//GEN-LAST:event_nombreslistaPopupMenuWillBecomeInvisible

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        int resultadoConfirmacion = JOptionPane.showConfirmDialog(this, "¬øDeseas eliminar el ciudadano"
                + " / planeta seleccionado?", "Eliminar", JOptionPane.INFORMATION_MESSAGE);
        if (resultadoConfirmacion == 0) {

            String nombre = (String) nombreslista.getSelectedItem();

            if (filtro.getSelectedItem().equals("Planetas")) {
                System.out.println("EL FILTRO ES DE TIPO PLANETA");
                Planeta p = getHashCodePlanet(new Planeta(nombre));
                System.out.println(p);
                allplanet.remove(p);
                dispose();

            } else if (filtro.getSelectedItem().equals("Ciudadanos")) {
                System.out.println("EL FILTRO ES DE TIPO CIUDADANOS");
                Ser s = getHashCodeSer(new Ser(nombre));
                for (Planeta p : allplanet) {
                    if (p.getPopulation().contains(s)) {
                        System.out.println("Se ha encontrado el planeta: " + p);
                        p.getPopulation().remove(s);

                    }
                    dispose();
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

