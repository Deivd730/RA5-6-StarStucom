    private void buscarciudadanoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarciudadanoActionPerformed

        String nombre = (String) nombresserlista.getSelectedItem();
        Ser s = getHashCodeSer(new Ser(nombre));

        if (s instanceof Vulcaniano) {

            System.out.println("SE HA ABIERTO LE VENTANA MODIFICAR VULCANIANO");
            ModificarVulcaniano mdfvul = new ModificarVulcaniano(new javax.swing.JDialog(), true, new Vulcaniano((String) nombresserlista.getSelectedItem()));
            mdfvul.setLocationRelativeTo(null);
            mdfvul.setVisible(true);

        } else if (s instanceof Andoriano) {

            System.out.println("SE HA ABIERTO LE VENTANA MODIFICAR ANDORIANO");
            ModificarAndoriano mdfan = new ModificarAndoriano(new javax.swing.JDialog(), true, new Andoriano((String) nombresserlista.getSelectedItem()));
            mdfan.setLocationRelativeTo(null);
            mdfan.setVisible(true);

        } else if (s instanceof Klingon) {

            System.out.println("SE HA ABIERTO LE VENTANA MODIFICAR KLINGON");
            ModificarKlingon mdfki = new ModificarKlingon(new javax.swing.JDialog(), true, new Klingon((String) nombresserlista.getSelectedItem()));
            mdfki.setLocationRelativeTo(null);
            mdfki.setVisible(true);

        } else if (s instanceof Nibiriano) {

            System.out.println("SE HA ABIERTO LE VENTANA MODIFICAR NIBIRIANO");
            ModificarNibiriano mdfnib = new ModificarNibiriano(new javax.swing.JDialog(), true, new Nibiriano((String) nombresserlista.getSelectedItem()));
            mdfnib.setLocationRelativeTo(null);
            mdfnib.setVisible(true);

        } else if (s instanceof Ferengi) {
            ModificarFerengi mdffe = new ModificarFerengi(new javax.swing.JDialog(), true, new Ferengi((String) nombresserlista.getSelectedItem()));
            mdffe.setLocationRelativeTo(null);
            mdffe.setVisible(true);

        } else if (s instanceof Humano) {

            System.out.println("SE HA ABIERTO LA VENTANA MODIFICAR HUMANO");
            ModificarHumano modificarhumano = new ModificarHumano(new javax.swing.JDialog(), true, new Humano((String) nombresserlista.getSelectedItem()));
            modificarhumano.setLocationRelativeTo(null);
            modificarhumano.setVisible(true);
//            System.out.println(new Humano((String) nombresserlista.getSelectedItem()).getName());
        }

    }//GEN-LAST:event_buscarciudadanoActionPerformed

